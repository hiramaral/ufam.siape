    private void initOrderAgent(String host) {
        jade.core.Runtime rt = jade.core.Runtime.instance();
        ContainerController cc = rt.createAgentContainer(new ProfileImpl(host, 1099, null, false));
        oa = new OrderAgent(this);
        try {
            AgentController ac = cc.acceptNewAgent("order", oa);
            ac.start();
        } catch (StaleProxyException ex) {
            System.out.println("Not start OrderAngent. Exception: " + ex);
            System.exit(-1);
        }
    }

