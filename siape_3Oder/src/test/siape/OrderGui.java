    public void refreshSkillsDone(String[] letras) {
        StringBuilder sb = new StringBuilder();
        for (String letra : letras) {
            sb.append(letra).append(" ");
        }
        AvailablesTxt.setText(sb.toString());
    }

    public void anagramDone(String anagrama, boolean failure) {
        DefaultListModel model;
        javax.swing.JList list;

        model = (DefaultListModel) (orderList.getModel());
        model.remove(0);
        orderList.updateUI();

        if (failure) {
            list = failureList;
        } else {
            list = producedList;
        }

        model = (DefaultListModel) (list.getModel());
        model.addElement(anagrama);
        list.updateUI();
    }

    public void removeListOrder() {
        int index = orderList.getSelectedIndex();
        if (index != 0) {
            DefaultListModel model = (DefaultListModel) orderList.getModel();
            model.remove(index);
        }
        orderList.updateUI();
    }

